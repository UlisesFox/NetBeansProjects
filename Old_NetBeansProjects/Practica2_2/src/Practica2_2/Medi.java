package Practica2_2;

public class Medi {

    private int Code;
    private String Nombre;
    private String Precio;

    public int getCodigo() {
        return Code;
    }

    public void setCodigo(int id) {
        this.Code = id;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String nombre) {
        this.Nombre = nombre;
    }

    public String getPrecio() {
        return Precio;
    }

    public void setPrecio(String version) {
        this.Precio = version;
    }

}